public class Operators {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 10;
        int sum = num1 + num2;
        System.out.println(sum);

        int num3 = 10;
        int num4 = 5;
        int diff = num3 - num4;
        System.out.println(diff);
        
        int num5 = 5;
        int num6 = 3;
        int mul = num5 * num6;
        System.out.println(mul);

        int num7 = 6;
        int num8 = 2;
        int div = num7 / num8;
        System.out.println(div);
    }
}